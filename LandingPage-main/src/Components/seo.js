import React from 'react';
import Head from 'next/head';

export default function SEO({
  description = 'Have you ever been in a situation where you just couldn’t use the keyboard or mouse on your computer? For any reason? Well, we’ve all been there! HandsFree is an AI-based program created to help you navigate your computer with no physical touch at all!',
  author = 'Farhan Rehman',
  meta,
  title = 'HandsFree',
}) {
  const metaData = [
    {
      name: `description`,
      content: description,
    },
    {
      property: `og:title`,
      content: title,
    },
    {
      property: `og:description`,
      content: description,
    },
    {
      property: `og:type`,
      content: `website`,
    },
    {
      name: `twitter:card`,
      content: `summary`,
    },
    {
      name: `twitter:creator`,
      content: author,
    },
    {
      name: `twitter:title`,
      content: title,
    },
    {
      name: `twitter:description`,
      content: description,
    },
  ].concat(meta);
  return (
    <Head>
      <title>{title}</title>
      {metaData.map(({ name, content }, i) => (
        <meta key={i} name={name} content={content} />
      ))}
    </Head>
  );
}

SEO.defaultProps = {
  lang: `en`,
  meta: [],
};
